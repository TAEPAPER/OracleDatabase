  CREATE OR REPLACE FUNCTION "HR"."FN_SUM_BUYAMT" (
        P_YEAR CHAR, P_PID PROD.PROD_ID%TYPE)
         RETURN NUMBER
         IS
         V_SAMT NUMBER :=0;
         BEGIN
         SELECT SUM(BUY_QTY*BUY_COST) INTO V_SAMT
