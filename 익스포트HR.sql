  CREATE OR REPLACE FUNCTION "HR"."FN_AMT_BUYQTY" (
        P_YEAR CHAR, P_PID PROD.PROD_ID%TYPE
         )
         RETURN NUMBER;
         V_SQTY NUMBER :=0;
         BEGIN
         SELECT SUM(BUY_QTY) INTO V_SQTY
         FROM BUYPROD
         WHERE EXTRACT(YEAR FROM BUY_DATE)= P_YEAR
             AND BUY_PROD = P_PID;
        RETURN V_SQTY;
        END;

