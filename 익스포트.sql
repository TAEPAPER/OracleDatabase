       P_PERIOD IN VARCHAR2)
       IS 
       V_SDATE DATE :=TO_DATE(P_PERIOD||'01');
       V_EDATE DATE :=LAST_DAY(V_SDATE);
       V_QTY NUMBER :=0;
       CURSOR CUR_BUY02
       IS
