                SUM(BUY_QTY) AS BAMT
           FROM BUYPROD
           WHERE BUY_DATE BETWEEN V_SDATE AND V_EDATE
           GROUP BY BUY_PROD;
    BEGIN 
        FOR REC IN CUR_BUY02 LOOP
          UPDATE REMAIN 
             SET REMAIN_I=REMAIN_I + REC.BAMT,
              REMAIN_J_99= REMAIN_J_99 +REC.BAMT,
              REMAIN_DATE = V_EDATE
              WHERE REMAIN_YEAR = SUBSTR(P_PERIOD,1,4)
                    AND PROD_ID = REC.BID;
                COMMIT;
        END LOOP;
    END;

