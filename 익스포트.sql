                FROM CART A, PROD B
                WHERE CART_NO LIKE V_PERIOD
                  AND A.CART_PROD = B.PROD_ID
                  GROUP BY A.CART_MEMBER
                  ORDER BY 2 DESC) TBL
        WHERE ROWNUM =1;

        SELECT MEM_NAME, MEM_ADD1||' '||MEM_ADD2,MEM_MILEAGE
        INTO P_NAME, P_ADDR,P_MILE
        FROM MEMBER
        WHERE MEM_ID = P_MID;
